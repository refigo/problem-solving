#include <bits/stdc++.h>
using namespace std;
string s;
int main() {
	cin.tie(0); cout.tie(0); ios::sync_with_stdio(0);
	cin >> s;
	if (s == "SONGDO") {
		cout << "HIGHSCHOOL";
	} else if (s == "CODE") {
		cout << "MASTER";
	} else if (s == "2023") {
		cout << "0611";
	} else if (s == "ALGORITHM") {
		cout << "CONTEST";
	}
	return 0;
}